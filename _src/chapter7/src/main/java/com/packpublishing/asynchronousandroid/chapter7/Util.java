package com.packpublishing.asynchronousandroid.chapter7;

import android.content.Context;

import java.io.InputStream;

public class Util {

    public static  String loadJSONFromFile(Context ctx,String filename) {
        String json = null;
        try {
            InputStream is = ctx.openFileInput(filename);
            int size = is.available();
            byte[] buffer = new byte[size];
            is.read(buffer);
            is.close();
            json = new String(buffer, "UTF-8");
        } catch (Exception ex) {
            ex.printStackTrace();
            return null;
        }
        return json;
    }
}
